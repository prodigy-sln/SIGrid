    /// </summary>
    OneMinute = 60,

    /// <summary>
    /// 3m
    /// </summary>
    ThreeMinutes = 60 * 3,

    /// <summary>
    /// 5m
    /// </summary>
    FiveMinutes = 60 * 5,

    /// <summary>
    /// 15m
    /// </summary>
    FifteenMinutes = 60 * 15,

    /// <summary>
    /// 30m
    /// </summary>
    ThirtyMinutes = 60 * 30,

